fn fmt_spice(f: &mut Formatter<'_>, short: &str, long: &str) -> std::fmt::Result {
    let mut msg = String::new();
    msg += "SPICE(";
    msg += short;
    msg += ")";

    let mut expln = " ".repeat(256);
    crate::generated::spicelib::api::expln(&msg, &mut expln);
    let expln = expln.trim_ascii_end();

    if !expln.is_empty() {
        msg += " (";
        msg += expln;
        msg += ")";
    }

    msg += ": ";
    msg += long;

    f.write_str(&msg)
}

